function X=flipall(X)
%若X(:,:,1)=[1 2;3 4],X(:,:,2)=[5 6;7 8],X(:,:,3)=[9 10;11 12],则
%flip(X,1)后，X(:,:,1)=[3 4;1 2],X(:,:,2)=[7 8;5 6],X(:,:,3)=[11 12;9 10]
%flip(X,2)后，X(:,:,1)=[2 1;4 3],X(:,:,2)=[6 5;8 7],X(:,:,3)=[12 11;10 9]
%flip(X,3)后，X(:,:,1)=[9 10;11 12]，X(:,:,2)=[5 6;7 8]，X(:,:,3)=[1 2;3 4]

%对矩阵的每一维度都进行翻转
for i=1:1:ndims(X);
    X=flipdim(X,i);
end

end